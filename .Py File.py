def predict_consumption(input_data):
    feature_vector = []
    for col in top_features:
        if col in input_data:
            feature_vector.append(input_data[col])
        else:
            feature_vector.append(0)

    features_scaled = scaler_final.transform([feature_vector])
    prediction = best_model.predict(features_scaled)[0]
    return prediction

def generate_recommendations(input_data, prediction, unit_price=35):
    recommendations = []

    ac_hours = input_data.get('AC_Usage_Hours', 0)
    if ac_hours > 8:
        recommendations.append({
            'category': 'Air Conditioner',
            'recommendation': 'Reduce AC usage to 6-8 hours/day. Set temperature to 26°C and use timer.',
            'potential_savings': '5-10% of daily consumption'
        })

    fan_hours = input_data.get('Fan_Usage_Hours', 0)
    if fan_hours > 12:
        recommendations.append({
            'category': 'Fans',
            'recommendation': 'Reduce fan usage. Use ceiling fans instead of standing fans.',
            'potential_savings': '2-5% of daily consumption'
        })

    wash_hours = input_data.get('Washing_Machine_Usage_Hours', 0)
    if wash_hours > 1.5:
        recommendations.append({
            'category': 'Washing Machine',
            'recommendation': 'Use cold water wash cycle. Wait for full load before running.',
            'potential_savings': '2-3% of daily consumption'
        })

    light_hours = input_data.get('Lighting_Hours', 0)
    if light_hours > 10:
        recommendations.append({
            'category': 'Lighting',
            'recommendation': 'Switch to LED bulbs. Use natural lighting during daytime.',
            'potential_savings': '1-3% of daily consumption'
        })

    temp = input_data.get('Outdoor_Temperature_C', 0)
    if temp > 35:
        recommendations.append({
            'category': 'Temperature Management',
            'recommendation': 'High temperature detected. Pre-cool early morning. Use curtains to block heat.',
            'potential_savings': '3-8% of daily consumption'
        })

    recommendations.append({
        'category': 'General Tips',
        'recommendation': 'Monitor peak usage hours (6-10 PM). Shift heavy loads to off-peak times.',
        'potential_savings': '5-10% of daily consumption'
    })

    monthly_consumption = prediction * 30
    monthly_cost = monthly_consumption * unit_price

    recommendations.append({
        'category': 'Cost Analysis',
        'recommendation': f'Current monthly cost: Rs. {monthly_cost:,.2f}. '
                         f'Saving 10% could reduce bill by Rs. {monthly_cost * 0.1:,.2f}/month.',
        'potential_savings': '10-15% of monthly bill'
    })

    return recommendations

